class Addition 
{
	public static void main(String[] args) 
	{


		byte a = 10 ; 
		byte b = 20 ; 
		
		// byte sum1 = a + b;  CTE
		
		int sum1 = a + b;
		
		System.out.println(sum1);
		
		short c = 10 ; 
		short d = 30 ; 
		
		// short sum2 = c + d ; CTE
		
		int sum2 = c + d;
		
		System.out.println(sum2);
		
		
		int e = 10 ; 
		int f = 10 ; 
		
		int sum3 = e + f ;
		
		System.out.println(sum3);
		
		
		long g = 20;
		long h = 40 ; 
		
		long sum4 = g + h ; 
		
		System.out.println(sum4);
		
		float i = 3.5f;
		float j = 4.5f;
		
		float sum5 = i + j ; 
		System.out.println(sum5);
		
		
		char ch1 = 'a';
		char ch2 = 'b';
		
		int sum6 = ch1+ch2;
		
		System.out.println(sum6);
		
		
		System.out.println(10+"java"+20+10);
		System.out.println(10+20+"java"+20+10);
		
	   System.out.println(10+"python"+20+"java"+(20+10));
	}
}
