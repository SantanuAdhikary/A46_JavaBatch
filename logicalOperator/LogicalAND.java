class LogicalAND
{
	public static void main(String[] args) 
	{
		
		int age = 21 ; 
		
		int money = 50000;
		
		System.out.println(age>=18 && money>=20000);
		
		System.out.println(age>=18 && money>=100000);
		
		System.out.println(age!=21 && money==50000);
		
		System.out.println(age<20 && money<30000);
		
	}
}
