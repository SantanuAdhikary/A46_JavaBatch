class LogicalOR 
{
	public static void main(String[] args) 
	{
		
		int marks = 75 ; 
		int age = 18 ; 
		
		
		System.out.println(marks>50  ||  age > 15);
		
		System.out.println(marks==75 || age!=18);
		
		System.out.println(marks>=90 || age==18);
		
		System.out.println(marks>=80 || age <=10);
	}
}
